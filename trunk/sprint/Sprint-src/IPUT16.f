      SUBROUTINE IPUT16(IWRD,IVAL1,IVAL2)
C
C     PACKS TWO 16 BIT QUANTITIES INTO A 32 BIT (OR BIGGER) WORD
C
      IF (IVAL1.GT.32767 .OR. IVAL1.LT.-32768) THEN
         WRITE(*,*)'***INTERNAL PACKING ERROR IN IPUT16. IVAL1=',IVAL1
         STOP
      END IF
      IF (IVAL2.GT.32767 .OR. IVAL2.LT.-32768) THEN
         WRITE(*,*)'***INTERNAL PACKING ERROR IN IPUT16. IVAL2=',IVAL2
         STOP
      END IF
      CALL SBYTES(IWRD,IVAL1,0,16,0,1)
      CALL SBYTES(IWRD,IVAL2,16,16,0,1)

      RETURN

      END
